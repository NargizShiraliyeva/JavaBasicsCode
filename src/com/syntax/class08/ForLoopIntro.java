package com.syntax.class08;

public class ForLoopIntro {

	public static void main(String[] args) {
		
		
		for(int i=20; i>=1; i-=2) {
			
			System.out.println(i);
		}
		
	for (int i=20; i<=50; i++) {
		if(i%2==1)
		
		System.out.print(i+" ");
	}
	
	for (int f = 21; f <= 50; f += 2) {
		System.out.println(f + " ");
	}
	
	
	}
}
